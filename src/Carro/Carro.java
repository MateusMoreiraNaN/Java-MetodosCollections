package Carro;

public class Carro {

    private int rodas;
    private int portas;

    public Carro(int rodas, int portas){
        this.rodas = rodas;
        this.portas = portas;
    }

    public Carro(){

    }

    public  int getRodas(){
        return rodas;
    }

    public  int getPortas(){
        return portas;
    }


}
